    private void genAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_genAActionPerformed
        model.setxA(randomBigInteger(DEF_ORDER).toString());
        xA.setText(model.getxA());
        try {
            Point key = Home.ECC_CURVE.pow(Home.BASE, new BigInteger(model.getxA()));
            model.setyA(key);
            yAX.setText(key.getX().toString());
            yAY.setText(key.getY().toString());
            setSignature();
        } catch (Exception ex) {
            Logger.getLogger(EccDsaView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_genAActionPerformed

