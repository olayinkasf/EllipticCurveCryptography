    private void fileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fileMouseClicked
        JFileChooser chooser = new JFileChooser();
        chooser.showDialog(mParent, "Select");
        if (chooser.getSelectedFile() == null) {
            return;
        }
        model.setFile(chooser.getSelectedFile());
        file.setText(model.getFile().getAbsolutePath());
        try {
            fileHash.setText(Utils.getMD5Checksum(model.getFile().getAbsolutePath()));
            setSignature();
        } catch (Exception ex) {
            Logger.getLogger(EccDsaView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_fileMouseClicked

    private void setSignature() {
        model.sign();
        s1.setText(model.getS1());
        s2.setText(model.getS2());
        try {
            model.validate();
            u1.setText(model.getU1());
            u2.setText(model.getU2());
            s.setText(model.getS());
        } catch (Exception ex) {
            Logger.getLogger(EccDsaView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

